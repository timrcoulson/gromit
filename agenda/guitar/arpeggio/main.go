type ArpeggioPosition int

const (
	Dom7Pos1 ArpeggioPosition = iota
	Dom7Pos2
	Dom7Pos3
	Dom7Pos4
	Dom7Pos5
	Maj7Pos1
	Maj7Pos2
	Maj7Pos3
	Maj7Pos4
	Maj7Pos5
	Min7Pos1
	Min7Pos2
	Min7Pos3
	Min7Pos4
	Min7Pos5
	Dim7Pos1
	Dim7Pos2
)

func (e ArpeggioPosition) String() string {
	switch e {
	case Dom7Pos1, Dom7Pos2, Dom7Pos3, Dom7Pos4, Dom7Pos5:
		return "Dominant 7"
	case Maj7Pos1, Maj7Pos2, Maj7Pos3, Maj7Pos4, Maj7Pos5:
		return "Major 7"
	case Min7Pos1, Min7Pos2, Min7Pos3, Min7Pos4, Min7Pos5:
		return "Minor 7"
	case Dim7Pos1, Dim7Pos2:
		return "Diminished 7"
	default:
		return "Unknown"
	}
}


